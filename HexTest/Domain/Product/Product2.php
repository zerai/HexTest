<?php

namespace HexTest\Domain\Product;

// src/Product.php

// Conficurazione standard di un entity seconodo la guida doctrine

class Product2
{
    /**
     * @var int
     */
    protected $id;
    /**
     * @var string
     */
    protected $name;

    public function getId()
    {
        return $this->id;
    }

    public function getName()
    {
        return $this->name;
    }

    public function setName($name)
    {
        $this->name = $name;
    }
}
